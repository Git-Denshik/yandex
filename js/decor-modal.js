let open_sharik = document.getElementById('open_sharik')
let sharik = document.getElementById('sharik')
let close_sharik = document.getElementById('close_sharik')

open_sharik.addEventListener('click', function(event) {
    event.preventDefault()
    sharik.style.display = 'block'
})

close_sharik.addEventListener('click', function(event) {
    event.preventDefault()
    sharik.style.display = 'none'
})

